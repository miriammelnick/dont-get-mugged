from temboo.core.choreography import Choreography
from temboo.core.choreography import InputSet
from temboo.core.choreography import ResultSet
from temboo.core.choreography import ChoreographyExecution


class CreateCoupon(Choreography):

    """
    Create a new instance of the CreateCoupon Choreography. A TembooSession object, containing a valid
    set of Temboo credentials, must be supplied.
    """
    def __init__(self, temboo_session):
        Choreography.__init__(self, temboo_session, '/Library/Stripe/CreateCoupon')


    def new_input_set(self):
        return CreateCouponInputSet()

    def _make_result_set(self, result, path):
        return CreateCouponResultSet(result, path)

    def _make_execution(self, session, exec_id, path):
        return CreateCouponChoreographyExecution(session, exec_id, path)

"""
An InputSet with methods appropriate for specifying the inputs to the CreateCoupon
choreography. The InputSet object is used to specify input parameters when executing this choreo.
"""
class CreateCouponInputSet(InputSet):
        """
        Set the value of the APISecretKey input for this choreography. ((string) The secret API Key providied by Stripe)
        """
        def set_APISecretKey(self, value):
            InputSet._set_input(self, 'APISecretKey', value)

        """
        Set the value of the CouponId input for this choreography. ((optional, string) The unique identifier of your choice for the coupon. If not specified, a random code will be generated by Stripe.)
        """
        def set_CouponId(self, value):
            InputSet._set_input(self, 'CouponId', value)

        """
        Set the value of the DurationInMonths input for this choreography. ((optional, integer) If Duration is set to 'repeating', specify the number of months the discount will be valid.)
        """
        def set_DurationInMonths(self, value):
            InputSet._set_input(self, 'DurationInMonths', value)

        """
        Set the value of the Duration input for this choreography. ((string) Indicates how long the discount will be in effect. Valid values are: forever, once, repeating.)
        """
        def set_Duration(self, value):
            InputSet._set_input(self, 'Duration', value)

        """
        Set the value of the MaxRedemptions input for this choreography. ((optional, integer) Specify the number times the coupon can be used.)
        """
        def set_MaxRedemptions(self, value):
            InputSet._set_input(self, 'MaxRedemptions', value)

        """
        Set the value of the PercentOff input for this choreography. ((integer) A integer between 1 and 100 that represents the discount that the coupon will apply when used by a customer)
        """
        def set_PercentOff(self, value):
            InputSet._set_input(self, 'PercentOff', value)

        """
        Set the value of the RedeemBy input for this choreography. ((optional, date) An epoch timestamp in seconds representing the the last time at which the coupon can be used. Specify timestamp in UTC.)
        """
        def set_RedeemBy(self, value):
            InputSet._set_input(self, 'RedeemBy', value)


"""
A ResultSet with methods tailored to the values returned by the CreateCoupon choreography.
The ResultSet object is used to retrieve the results of a choreography execution.
"""
class CreateCouponResultSet(ResultSet):
        """
        Retrieve the value for the "Response" output from this choreography execution. ((XML) The response from Stripe)
        """
        def get_Response(self):
            return self._output.get('Response', None)

class CreateCouponChoreographyExecution(ChoreographyExecution):
    
    def _make_result_set(self, response, path):
        return CreateCouponResultSet(response, path)
